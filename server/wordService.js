// Build complete valid word set (answers + extra valid guesses)
const answerSet = new Set(ANSWERS.filter(w => w.length === 5));
const validSet = new Set([...ANSWERS, ...EXTRA_VALID].filter(w => w.length === 5));

// Filter answers to only valid 5-letter words
const answerList = [...answerSet];

/**
 * Select a random word from the answer list, excluding already-used words.
 */
function selectWord(usedWords = []) {
    const used = new Set(usedWords.map(w => w.toUpperCase()));
    const available = answerList.filter(w => !used.has(w));
    if (available.length === 0) {
        // Fallback: allow reuse if all words exhausted
        return answerList[Math.floor(Math.random() * answerList.length)];
    }
    return available[Math.floor(Math.random() * available.length)];
}

/**
 * Check if a word is in the valid-guess set.
 */
function isValidGuess(word) {
    return validSet.has(word.toUpperCase());
}

/**
 * Get the total number of available answers.
 */
function getAnswerCount() {
    return answerList.length;
}

